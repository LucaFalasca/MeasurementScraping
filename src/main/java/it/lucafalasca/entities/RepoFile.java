package it.lucafalasca.entities;

public class RepoFile {
    private String path;
    private String type;
    private String sha;
    private String size;
    private String url;

    public String getPath() {
        return path;
    }

    public String getType() {
        return type;
    }

    public String getSha() {
        return sha;
    }

    public String getSize() {
        return size;
    }

    public String getUrl() {
        return url;
    }
}
